public class OperadoresRelacionales {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        //Operaciones relacionales
        System.out.println("a == b: " + (a == b));
        System.out.println("a < b: " + (a < b));
        System.out.println("a <= b: " + (a <= b));
        System.out.println("a > b: " + (a >= b));
        System.out.println("a >= b: " + (a >= b));
        System.out.println("a != b: " + (a != b));
        /*
        Recuerda que los operadores relacionales sirven
        para comparar valores y obtener resultados logicos,
        estos influyen en la toma de decisiones de nuestra
        computadora.
         */
    }
}
